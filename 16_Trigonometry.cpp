#include<iostream>
#include<cmath>
using namespace std ;
int main()
{
    double theta = 45 ;
    cout <<"The value of sin(theta) is " << sin(theta) <<endl ;
    cout <<"The value of cos(theta) is " << cos(theta) <<endl ;
    cout <<"The value of tan(theta) is " << tan(theta) <<endl ;
    cout <<"The value of cot(theta) is " << 1/tan(theta) <<endl ;
    cout <<"The value of sec(theta) is " << 1/cos(theta) <<endl ;
    cout <<"The value of cosec(theta) is " << 1/sin(theta) <<endl ;
    return 0 ;

}