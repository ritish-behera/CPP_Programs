#include <iostream>
using namespace std;
int main()
{
   int a=10 ;
   int b=5 ;
   int sum = a + b ;
   cout << "sum :" << sum <<endl;
   int diff = a - b ;
   cout << "Difference :" << diff <<endl;
   int prod = a * b ;
   cout << "Product :" << prod <<endl;
   int div = a/b ;
   cout << "Division :" << div <<endl;

   return 0;
}